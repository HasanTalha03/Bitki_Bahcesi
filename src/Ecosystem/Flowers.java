package Ecosystem;

public class Flowers {
	
	
	private int number_of_roses=0;   //gul
	private int number_of_daisy=0;  // papatya 
	private int number_of_violet=0;  // menekse
	private int number_of_lily=0;   // zambak
	
	Flowers(int rose,int daisy,int violet,int lily){
		
		this.number_of_roses=rose;
		this.number_of_daisy=daisy;
		this.number_of_violet=violet;
		this.number_of_lily=lily;
		
	}
	
	
	
	
	
	

	public int getNumber_of_roses() {
		return number_of_roses;
	}

	public void setNumber_of_roses(int number_of_roses) {
		this.number_of_roses = number_of_roses;
	}

	public int getNumber_of_daisy() {
		return number_of_daisy;
	}

	public void setNumber_of_daisy(int number_of_daisy) {
		this.number_of_daisy = number_of_daisy;
	}

	public int getNumber_of_violet() {
		return number_of_violet;
	}

	public void setNumber_of_violet(int number_of_violet) {
		this.number_of_violet = number_of_violet;
	}

	public int getNumber_of_lily() {
		return number_of_lily;
	}

	public void setNumber_of_lily(int number_of_lily) {
		this.number_of_lily = number_of_lily;
	}
	

	
	
	
	
	
	
	
	

}
